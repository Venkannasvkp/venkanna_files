package basicsofjava;

public class AssignmentExample {

	public static void main(String[] args) 
	{
      int a=10;
      //PlusEalto
      a+=10;  //a=a+b
      System.out.println("+= is:"+a);
      //subractionEqualto
      a-=10;
      System.out.println("-= is:"+a);
      //MultiplicationEqualto
      a*=2;
      System.out.println("*= is:"+a);
      //Divison
      a/=5;
      System.out.println("/= is:"+a);
      //Modulusa and assign
      a%=2;
      System.out.println("%= is:"+a);
	}

}
