package basicsofjava;

public class RelationalExample {

	public static void main(String[] args) 
	{
		int a=10, b=15;
		//Equal to operator
		System.out.println("Equal to:"+(a==b));
		//NotEqual to operator
		System.out.println("Noteqaul:"+(a!=b));
		//Lessthan operator
		System.out.println("Lessthan:"+(a<b));
		//Graterthan operator
		System.out.println("Graterthan:"+(a>b));
		//LessthanEqualto
		System.out.println("LessthanEqual:"+(a<=b));
		//GraterthanEqualto
		System.out.println("GraterthyanEqual:"+(a>=b));
	}
}
