package basicsofjava;

public class LogicalExample {

	public static void main(String[] args) {
		
		int a=10,b=15,c=13;
		
	// Logical AND
		System.out.println("LOgical AND:"+(a<b&&b<c));
	// Logical OR
		System.out.println("Logical OR:"+(a<b||b<c));
	// Logical NOT
		System.out.println("Logical NOT:"+!(a<b));

	}

}
