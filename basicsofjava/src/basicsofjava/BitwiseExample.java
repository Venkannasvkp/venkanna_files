package basicsofjava;

public class BitwiseExample {

	public static void main(String[] args) 
	{
	   	int a=5;
	   	int b=6;
	   	System.out.println("we are (a&b):"+ (a&b));
	 	System.out.println("we are (a||b):"+ (a|b));
	 	System.out.println("we are (a^b):"+ (a^b));
	 	System.out.println("we are (~a):"+ (~a));
	 	System.out.println("we are (a<<2):"+ (a<<2));
	 	System.out.println("we are (a>>2):"+ (a>>2));
	}
	}
